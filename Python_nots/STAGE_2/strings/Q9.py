#Q9: **Pading** and align a title using center()
#Make it look like this — total length = 10 characters, padded with *
#**Welcome**

title = "Welcome"
print(title.center(11,'*'))     #**Welcome**   
print(title.ljust(11,'*'))      #Welcome****
print(title.rjust(11,'*'))      #****Welcome

#center(10, '*')→  pads on the both side
#ljust(10, '*') → pads on the right side
#rjust(10, '*') → pads on the left side


